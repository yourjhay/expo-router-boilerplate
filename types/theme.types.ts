export type ThemeProps = {
  /**
   * the color used for the light mode
   */
  lightColor?: string;

  /**
   * the color used for the dark mode
   */
  darkColor?: string;
};

export type InputThemeProps = {
  /**
   * the input text color used for the light mode
   */
  lightTextColor?: string;

  /**
   * the input text color used for the dark mode
   */
  darkTextColor?: string;
};
